<div class="container">
  <h1>Ce este Algorift?</h1>

  <p>
    Algorift este o platformă cu probleme de informatică bazată pe principiul:<br
    />
    rezolă - încarcă - verifică.
  </p>
  <p>
    Deși ideea este destul de stabilită, existând deja câteva alternative,
    Algorift vine cu multe elemente noi, de actualitate. Folosind tehnologii de
    actualitate, această nouă platformă poate să aducă o nouă perspectivă asupra
    acestui domeniu și poate inova modul în care oamenii învață informatică.
  </p>
  <p>
    Algorift este construit să ofere un design intuitiv și atractiv, să fie
    extrem de rapid și eficient din punct de vedere al resurselor, dar și să fie
    ușor de hostat pe propriul server.
  </p>
  <p>
    Scopul Algorift este de a aduce un element distractiv în rezolvarea
    problemelor de informatică pentru a încuraja tineretul să abordeze acest
    domeniu de actualitate.
  </p>

